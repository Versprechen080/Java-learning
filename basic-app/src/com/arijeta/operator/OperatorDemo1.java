package com.arijeta.operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        //基本算术运算符的使用
        int a = 10;
        int b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println((double) a / b);
        //取余
        System.out.println(a % b);
    }
}
